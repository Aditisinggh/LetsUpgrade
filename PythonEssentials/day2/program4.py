#TUPLE and its default methods

myTup =(1,8,2,6,4,6,7,5,3)

#Print count of an element
print("Count of 6:",myTup.count(6))

#Print index of an element
print("Index of 3:",myTup.index(3))

#Size of tuple
print("Tuple size:",len(myTup))

#Printing sorted tuple
print("Sorted tuple:",sorted(myTup))

#Printing reversed tuple
print("Reversed tuple:",myTup[::-1])