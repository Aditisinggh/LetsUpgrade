#STRINGS and Default Methods

myStr= "This is my string"

#Changing string to all Uppercase
print("Uppercase:",myStr.upper())


#Changing string to all lowercase
print("Lowercase:",myStr.lower())


#Splitting all words in string separated by space  
print("Space separated words:",myStr.split(" "))

#To check is string is an alphanumeric string
print("Is alphanumeric:",myStr.isalnum())

#Replacing all 'i' by 'p'
print("Replacing i by p:",myStr.replace('i','p'))